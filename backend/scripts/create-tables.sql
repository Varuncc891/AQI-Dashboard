-- ALERTS TABLE AND VALUES INSERTION.
CREATE TABLE alerts (
    alert_id SERIAL PRIMARY KEY,
    sensor_id INTEGER REFERENCES sensors(sensor_id) ON DELETE CASCADE,
    alert_type VARCHAR(50) NOT NULL CHECK (alert_type IN ('traffic_congestion', 'air_quality', 'sensor_fault')),
    severity VARCHAR(20) NOT NULL CHECK (severity IN ('low', 'medium', 'high', 'critical')),
    message TEXT NOT NULL,
    timestamp TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    is_acknowledged BOOLEAN DEFAULT FALSE
);


-- READINGS TABLE, DATA INSERTION
CREATE TABLE readings (
    reading_id BIGSERIAL PRIMARY KEY,
    sensor_id INTEGER REFERENCES sensors(sensor_id) ON DELETE CASCADE,
    timestamp TIMESTAMPTZ NOT NULL,
    vehicle_count INTEGER NOT NULL CHECK (vehicle_count >= 0),
    avg_speed_kmh DECIMAL(5,1) CHECK (avg_speed_kmh >= 0),
    aqi_value INTEGER CHECK (aqi_value >= 0 AND aqi_value <= 500),
    pm25 DECIMAL(5,2) CHECK (pm25 >= 0),
    co2_ppm DECIMAL(6,2) CHECK (co2_ppm >= 0),
    dominant_vehicle_type_id INTEGER REFERENCES vehicle_types(type_id),
    weather_condition VARCHAR(50) CHECK (weather_condition IN ('clear', 'rainy', 'foggy', 'snowy', 'stormy', 'cloudy')),
    temperature DECIMAL(4,1),
    humidity DECIMAL(4,1)
);


